// MARK: - 统一品牌图标视图

struct BrandIcon: View {
    let source: SessionSource
    var size: CGFloat = 14

    var body: some View {
        switch source {
        case .claude:
            ClaudeLogoShape()
                .aspectRatio(1.0, contentMode: .fit)
                .frame(height: size)
        case .kimi:
            KimiLogoShape()
                .aspectRatio(24.0 / 25.0, contentMode: .fit)
                .frame(height: size)
        }
    }
}
